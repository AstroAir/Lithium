# Example 1: Start Nginx server
# This example starts the Nginx server.
$ python nginx.sh start

# Example 2: Stop Nginx server
# This example stops the Nginx server.
$ python nginx.sh stop

# Example 3: Reload Nginx configuration
# This example reloads the Nginx configuration.
$ python nginx.sh reload

# Example 4: Restart Nginx server
# This example restarts the Nginx server.
$ python nginx.sh restart

# Example 5: Test Nginx configuration
# This example tests the Nginx configuration without reloading.
$ python nginx.sh test

# Example 6: Check Nginx configuration syntax
# This example checks the Nginx configuration syntax.
$ python nginx.sh check

# Example 7: Show Nginx running status
# This example shows the running status of the Nginx server.
$ python nginx.sh status

# Example 8: Show Nginx version information
# This example shows the version information of the Nginx server.
$ python nginx.sh version

# Example 9: Backup Nginx configuration file
# This example backs up the Nginx configuration file.
$ python nginx.sh backup

# Example 10: Restore Nginx configuration from backup
# This example restores the Nginx configuration file from a backup.
$ python nginx.sh restore

# Example 11: View Nginx access logs
# This example views the Nginx access logs.
$ python nginx.sh view_logs access

# Example 12: View Nginx error logs
# This example views the Nginx error logs.
$ python nginx.sh view_logs error

# Example 13: Clear Nginx access logs
# This example clears the Nginx access logs.
$ python nginx.sh clear_logs access

# Example 14: Clear Nginx error logs
# This example clears the Nginx error logs.
$ python nginx.sh clear_logs error

# Example 15: List available and enabled sites
# This example lists all available and enabled sites in the Nginx configuration.
$ python nginx.sh list_sites

# Example 16: Show help message
# This example shows the help message with all available commands.
$ python nginx.sh help